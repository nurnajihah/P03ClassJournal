package sg.edu.rp.c346.p03_classjournal;

public class Modules {
    private String week;
    private String grade;

    public Modules(String week, String grade) {
        this.week = week;
        this.grade = grade;
    }

    public String getWeek() {
        return week;
    }

    public String getGrade() {
        return grade;
    }
}


